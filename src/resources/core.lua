function core.Colors.CodeToHex(code)
  return core.Colors.ansi[tonumber(string.sub(code, -2), 16)]
end

function core.Colors.TerrisToHex(msg)
  msg = string.gsub(msg, "%^[RrBbCcGgOoVvYyWwTtPpNJEeIiKZH]", core.Colors.codes)
  msg = string.gsub(msg, "%^J%w%w", core.Colors.CodeToHex)
  return msg
end

function core.Colors.Fix()
  local getLineRes = getCurrentLine()
  if getLineRes:sub(1,5) == "ERROR" then
    return
  end
  selectCurrentLine()
  local newline = string.gsub(getCurrentLine(), "%^[RrBbCcGgOoVvYyWwTtPpNJEeIiKZH]", core.Colors.codes)
  newline = string.gsub(newline, "%^J%w%w", core.Colors.CodeToHex)
  hreplace(newline)
end

